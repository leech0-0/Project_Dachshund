////////// Volume Controls      ////////////////////////////////////
////////////////////////////////////////////////////////////////////
var vslider = document.getElementById("volumeRange");

vslider.addEventListener("mousemove", function(){
    var x = vslider.value;
    var colour = 'linear-gradient(90deg, #5e81db ' + (x * 10) + '%, #DDDDDD ' + (x * 10) + '%)';
    vslider.style.background = colour;
})


